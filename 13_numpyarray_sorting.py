#sort()-the numpy ndarray object has a function which is called sort() and this will sort a specific array

import numpy as np
shared=np.array([5,8,6,0,1])
print(np.sort(shared)) #this method is like as copy()
 
#sorted the array Alphabetically

import numpy as np
shared=np.array(['banana','mango','litchi','apple'])
print(np.sort(shared))

#sort the boolean array
import numpy as np
shared=np.array([True,False,True,False])
